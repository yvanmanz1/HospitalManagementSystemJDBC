    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(idtxt.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter the Patient ID to be deleted");
        }
        else{
            Appoints Obj = new Appoints();
            Obj.setId(Integer.parseInt(idtxt.getText()));
            Dao dao = new Dao();
            dao.deleteappointment(Obj);
            String feedback = dao.deleteappointment(Obj);
            JOptionPane.showMessageDialog(this, feedback);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

