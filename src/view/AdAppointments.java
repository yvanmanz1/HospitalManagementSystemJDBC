     private void addColumnTableHeader(){
        tableModel.addColumn("Patient Id"); 
        tableModel.addColumn("Names");
        tableModel.addColumn("Email");
        tableModel.addColumn("Phone");
        tableModel.addColumn("Department");
        tableModel.addColumn("Doctor");
        tableModel.addColumn("Insurance");
        tableModel.addColumn("Day");
        tableModel.addColumn("Time");
        tableModel.addColumn("Status");
        
        appointmentsTable.setModel(tableModel);
    }
     private void addRowTableData(){
        tableModel.setRowCount(0);
        Dao dao = new Dao();       
        List<Appoints> users = dao.alltheAppointments();
        for(Appoints Obj : users){
            tableModel.addRow(new Object[]{
               Obj.getId(),
                Obj.getName(),
                Obj.getEmail(),
                 Obj.getPhone(),
                 Obj.getDepartment(),
                 Obj.getDoctor(),
                Obj.getInsurance(),
                Obj.getDay(),
                Obj.getTime(),
                Obj.getStatus()
            });
        }
        
    }
